/*
 * Returns with the html code for country select field
 */
function b4rcp_get_country_select_field( $default_value = '', $id = '', $name = '', $classes ='') {
  $countries = b4rcp_get_countries();

  $o = '<select name="'. $name .'" id="'. $id .'" class="'. $classes .'" >';
  foreach ($countries as $country_key => $country_name) {
    $o .= '<option value="'. $country_key .'" '. ($default_value == $country_key ? 'selected' : '') .'>';
      $o .= $country_name .' - '. $country_key;
    $o .= '</option>';
  }
  $o .= '</select>';

  return $o;
}
